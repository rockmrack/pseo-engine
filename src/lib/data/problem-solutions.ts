// Helper functions
export function getProblem(slug: string): Problem | undefined {
  return problems.find(p => p.slug === slug);
}

export function getProblemsByCategory(category: Problem['category']): Problem[] {
  return problems.filter(p => p.category === category);
}

export function getProblemsByUrgency(urgency: Problem['urgency']): Problem[] {
  return problems.filter(p => p.urgency === urgency);
}

export function getEmergencyProblems(): Problem[] {
  return problems.filter(p => p.urgency === 'emergency');
}

export function getAllProblems(): Problem[] {
  return problems;
}

// Generate URL params for all problems
export function generateProblemParams(): { problem: string }[] {
  return problems.map(p => ({ problem: p.slug }));
}
