// Helper function to get location by slug
export function getLocationBySlug(slug: string): Location | undefined {
  return locations.find(l => l.slug === slug);
}

// Helper function to get locations by area
export function getLocationsByArea(area: string): Location[] {
  return locations.filter(l => l.area.toLowerCase() === area.toLowerCase());
}

// Helper function to get locations by postcode
export function getLocationsByPostcode(postcode: string): Location[] {
  return locations.filter(l => l.postcode === postcode);
}

// Helper function to get nearby locations
export function getNearbyLocations(slug: string, count: number = 6): Location[] {
  const location = getLocationBySlug(slug);
  if (!location) return [];

  return location.nearbyStreets
    .map(streetSlug => getLocationBySlug(streetSlug))
    .filter((l): l is Location => l !== undefined)
    .slice(0, count);
}

// Get all unique areas
export function getAllAreas(): string[] {
  return Array.from(new Set(locations.map(l => l.area)));
}

// Get all unique postcodes
export function getAllPostcodes(): string[] {
  return Array.from(new Set(locations.map(l => l.postcode)));
}
